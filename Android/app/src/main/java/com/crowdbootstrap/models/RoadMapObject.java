public class RoadMapObject {
    private String roadmapName;
    private String id;
    private String deliverable_link;

    public String getRoadmapName() {
        return roadmapName;
    }

    public void setRoadmapName(String roadmapName) {
        this.roadmapName = roadmapName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeliverable_link() {
        return deliverable_link;
    }

    public void setDeliverable_link(String deliverable_link) {
        this.deliverable_link = deliverable_link;
    }
}
